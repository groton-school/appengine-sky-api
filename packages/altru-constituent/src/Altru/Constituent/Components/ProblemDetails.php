<?php

namespace Blackbaud\SKY\Altru\Constituent\Components;

use Battis\OpenAPI\Client\BaseComponent;

/**
 * A machine-readable format for specifying errors in HTTP API responses based
 * on https://tools.ietf.org/html/rfc7807.
 *
 * @property string $type A URI reference \[RFC3986\] that identifies the
 *   problem type. This specification encourages that, when dereferenced, it
 *   provide human-readable documentation for the problem type (e.g., using HTML
 *   \[W3C.REC-html5-20141028\]). When this member is not present, its value is
 *   assumed to be "about:blank".
 * @property string $title A short, human-readable summary of the problem
 *   type. It SHOULD NOT change from occurrence to occurrence of the problem,
 *   except for purposes of localization (e.g., using proactive content
 *   negotiation; see \[RFC7231\], Section 3.4).
 * @property int $status The HTTP status code (\[RFC7231\], Section 6)
 *   generated by the origin server for this occurrence of the problem.
 * @property string $detail A human-readable explanation specific to this
 *   occurrence of the problem.
 * @property string $instance A URI reference that identifies the specific
 *   occurrence of the problem. It may or may not yield further information if
 *   dereferenced.
 * @property string $trace_id A request ID that can be provided to Blackbaud
 *   Support that may help with further troubleshooting.
 * @property string $span_id A request ID that can be provided to Blackbaud
 *   Support that may help with further troubleshooting.
 *
 * @api
 */
class ProblemDetails extends BaseComponent
{
    /**
     * @var string[] $fields
     */
    protected static array $fields = [
        "type" => "string",
        "title" => "string",
        "status" => "int",
        "detail" => "string",
        "instance" => "string",
        "trace_id" => "string",
        "span_id" => "string",
    ];
}
